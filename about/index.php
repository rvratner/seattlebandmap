	<h2>About the Band Map</h2>

	<p>
		The Seattle Band Map is a project that showcases the northwest's vibrant music scene by documenting the thousands of bands who have performed throughout the decades; it also explores how these bands are interconnected through personal relationships and collaborations.
	</p>

	<p>
		This project aims to diversify the audience for and broaden the understanding of Seattle's music scene, while spotlighting unrepresented artists and musical genres. Seattle has long been known as a hotbed of musical creativity, from the thriving 60s and early 70s Soul and Funk scene, to the 90s grunge movement. Music continues to thrive in Seattle, and we see the Seattle Band Map as an opportunity to keep it in the forefront of people's minds.
	</p>
	
